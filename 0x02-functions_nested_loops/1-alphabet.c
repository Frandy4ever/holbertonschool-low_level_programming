#include "main.h"

/**
 * print_alphabet - Prints the alphabet, in lowercase with putchar
 *
 * Return: void
 */

void print_alphabet(void)

{

char frandy;

for (frandy - 'a'; frandy <= 'z'; frandy++)

{

_putchar(frandy);

}

_putchar('\n');

}
