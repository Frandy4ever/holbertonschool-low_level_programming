#include <stdio.h>

/**
 * main - Prints all single digit numbers of base 10 starting from 0
 *
 * Return: Always 0
 */

int main(void)

{

int frandy;

for (frandy = 0; frandy < 10; frandy++)

{

putchar(frandy);

}

putchar('\n');

return (0);

}
